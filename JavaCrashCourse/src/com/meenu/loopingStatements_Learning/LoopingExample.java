package com.meenu.loopingStatements_Learning;

public class LoopingExample {

	public static void main(String[] args) {
		
		/*
		 * for (int Times = 0; Times<3; Times++) {
		 * System.out.println("I can Program!! i can Learn."); }
		 */
		
		int times=0;
		/*
		 * while(times<3) { System.out.println("I can Program!! i can Learn."); times++;
		 * }
		 */
		
		do {
			System.out.println("I can Program!! i can Learn.");
			times++;
		} while (times<3);

	}

}
